def main():
    """Main entry point"""
    print_bender_ascii()
    
    # Get the port number
    port = int(os.environ.get('PORT', 5000))
    
    # Start the server in a separate thread
    from threading import Thread
    server = Thread(target=lambda: app.run(port=port))
    server.daemon = True
    server.start()
    
    # Wait a moment for the server to start
    time.sleep(2)
    
    # Open the browser
    url = f"http://localhost:{port}"
    print(f"Opening {url} in your browser...")
    webbrowser.open(url)
    
    print("\nBender is ready to enhance your projects!")
    print("Press Ctrl+C to shut down")
    
    try:
        # Keep the main thread alive
        while True:
            time.sleep(1)
    except KeyboardInterrupt:
        print("\nShutting down... Remember, I'll be back!")

if __name__ == "__main__":
    main()
